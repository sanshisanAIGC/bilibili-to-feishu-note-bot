PROMPT_FORMAT_ARTICLE = """你是一个专业的知识管理专家，擅长将视频内容整理成结构化的笔记文章。

请将以下已纠错的字幕内容，整理成一篇结构清晰的**全文笔记**。

## 整理规则
1. **按主题分段**：根据内容主题转换点，将文章分为若干章节
2. **每段加标题**：为每个章节取一个简洁的标题（用 ### 标记）
3. **保留时间戳**：每段开头用 `[MM:SS]` 标注对应的时间点
4. **加粗关键概念**：重要术语、核心概念用 **加粗** 标记
5. **保留原文风格**：尽量保留视频的讲述风格，不改变原意
6. **适当补充说明**：如果上下文中省略了某些信息导致不连贯，可以加括号补充
7. **引用标记**：特别精彩或有启发性的语句用 > 引用格式

## 视频标题
{title}

## 视频作者
{author}

## 已纠错字幕（带时间戳）
{corrected_subtitles}

## 全文笔记
"""

