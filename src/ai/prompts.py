PROMPT_CORRECT_SUBTITLES = """你是一个专业的文字编辑，擅长中文文本纠错和排版。

请对以下视频字幕进行纠错处理，注意：

## 纠错规则
1. **修正错别字和同音字**：语音识别常见的同音字错误（如"在/再"、"的/地/得"、"做/作"等）
2. **修正语法错误**：不通顺的句子改成通顺的表述
3. **去除口语化冗余**：删除无意义的语气词和重复（如"这个这个"、"嗯"、"啊"），但保留有意义的语气表达
4. **保持原意**：不要改变说话人的原意，不要添加原文没有的内容
5. **保留时间戳**：每个字幕片段都保留对应的时间戳
6. **合并过短片段**：如果一个句子被切分成过多小片段（时间间隔<2秒的相邻同主题内容），适当合并，让每条字幕成为完整的一句话

## 输入格式
字幕数据以 JSON 数组形式提供，每条包含：
- from: 开始时间（秒）
- to: 结束时间（秒）
- content: 字幕文本

## 输出格式
返回 JSON 数组，格式与输入相同，但 content 已纠错和优化。
直接返回 JSON，不要包含其他文字。

## 视频标题
{title}

## 字幕数据
{subtitles_json}

## 正确、通顺的字幕
"""

